start_time = log_step_start("step008x_timeline_kills_partidas_torneio")
df = pd.read_csv('golgg/torneios/CBLOL Cup 2026/CBLOL_Cup_2026.csv')
info_teams = pd.read_csv('golgg/source/info_teams.csv')

# Support current CSV headers generated by Step 1/Step 2.
df = df.rename(columns={'Partida': 'partida'})
info_teams = info_teams.rename(columns={'Player': 'player', 'Role': 'role', 'Team': 'nome_time'})


def to_timeline_url(match_link):
  url = str(match_link).strip()
  if 'page-summary/' in url:
    return url.replace('page-summary/', 'page-timeline/')
  if url.endswith('/'):
    return url + 'page-timeline/'
  return url + '/page-timeline/'


soloers = []
for index, partida in df.iterrows():
  # print(partida['partida'])
  url = to_timeline_url(partida['Link'])
  httpx = requests.get(url, headers=headers)
  string = httpx.text
  soup = BeautifulSoup(string, 'html.parser')
  table = soup.find_all("table", attrs={'class':'nostyle timeline trhover'})
  if len(table) == 0:
      continue
  rows = table[0].find_all("tr", recursive=False)
  li = rows[1:]
  for x in li:
      solo = {}
      chi = x.find_all("td", recursive=False)
      if len(chi[3]) == 1 and chi[4].find(attrs={'src':'../_img/kill-icon.png'}):
          # print (chi[2].text,"solo'd",chi[6].text)
          solo['torneio'] = soup.find("div", attrs={"class":"col-12 mt-4"}).findChild("a").text
          solo['player'] = chi[2].text
          if chi[2].text == '':
            solo['role'] = ''
            solo['team'] = ''
          else:
            solo['role'] = info_teams[info_teams['player'] == chi[2].text]['role'].values[0]
            solo['team'] = info_teams[info_teams['player'] == chi[2].text]['nome_time'].values[0]
          solo['partida'] = partida['partida']
          try:
            solo['teamWinner'] = soup.find(string=" - WIN").find_previous_sibling().text
          except:
            solo['teamWinner'] = soup.find(string=" - WIN ").find_previous_sibling().text
          solo['target'] = chi[6].text
          solo['target_role'] = info_teams[info_teams['player'] == chi[6].text]['role'].values[0]
          soloers.append(solo)

solo_kills = pd.DataFrame(soloers)

solo_kills = pd.DataFrame(solo_kills[['torneio', 'player', 'role', 'team', 'partida', 'teamWinner', 'target', 'target_role']].value_counts().to_frame('count'))
solo_kills
