lista_killers = all_kills['player'].unique()

sum_killers = []
for killer in lista_killers:
  if str(killer) != 'nan':
    sum_killer = {}
    sum_killer['player'] = killer
    sum_killer['role'] = all_kills[all_kills['player'] == killer]['role'].values[0]
    sum_killer['team'] = all_kills[all_kills['player'] == killer]['team'].values[0]
    sum_killer['kills'] = all_kills[all_kills['player'] == killer]['count'].sum()
    target_counts = all_kills[all_kills['player'] == killer].groupby(['target'])['count'].sum().sort_values(ascending=False)
    top_target = target_counts.index[0]
    top_target_count = target_counts.iloc[0]
    sum_killer['fregues'] = str(top_target) + ' x' + str(top_target_count)
    sum_killer['freguesrole'] = all_kills[all_kills['target'] == top_target]['target_role'].values[0]
    sum_killer['solokills'] = solo_kills[solo_kills['player'] == killer]['count'].sum()
    try:
      solo_target_counts = solo_kills[solo_kills['player'] == killer].groupby(['target'])['count'].sum().sort_values(ascending=False)
      top_solo_target = solo_target_counts.index[0]
      top_solo_target_count = solo_target_counts.iloc[0]
      sum_killer['solofregues'] = str(top_solo_target) + ' x' + str(top_solo_target_count)
      sum_killer['solofreguesrole'] = solo_kills[solo_kills['target'] == top_solo_target]['target_role'].values[0]
    except:
      sum_killer['solofregues'] = '-'
      sum_killer['solofreguesrole'] = '-'
    sum_killer['deaths'] = all_kills[all_kills['target'] == killer]['count'].sum()
    sum_killer['solodeaths'] = solo_kills[solo_kills['target'] == killer]['count'].sum()
    sum_killers.append(sum_killer)

sum_kills = pd.DataFrame(sum_killers)
sum_kills.to_csv("golgg/source/sum_kills.csv", index=False)
